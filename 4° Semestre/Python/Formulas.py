import math


def Hipotenusa(a,b):
    return math.sqrt(math.pow(a,2)+math.pow(b,2))
def CuadradoArea(l):
    return l*l
def CuadradoPerimetro(l):
    return l+l+l+l
def RectanguloArea(b,h):
    return b*h
def RectanguloPerimetro(b,h):
    return (b+h)*2
def TrainguloArea(b,h):
    return (b*h)/2
def TrainguloPerimetro(b,h):
    return b+h
def CirculoArea(r):
    return math.pow(r,2)
def CirculoPerimetro(d):
    return math.pow(d,2)*math.pi

